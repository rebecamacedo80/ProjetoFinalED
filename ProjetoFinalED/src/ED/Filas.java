    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       try{    
            if(f.cheia()){
                JOptionPane.showMessageDialog(null, "Fila Cheia");
            }else{    
                int valor = Integer.parseInt(jTextField1.getText());
                f.insere(valor);
                for(int i = f.primeiro(),n = 0; i<=f.ultimo();i++,n++){
                    buttons[n].setText(Integer.toString(f.valor(i)));
                }
                jTextField1.setText("");
            }
            buttons[0].setBackground(Color.GREEN);
        }catch(Exception e){
            //JOptionPane.showMessageDialog(null, "Ocorreu um erro.");
        }    
    }//GEN-LAST:event_jButton2ActionPerformed
        JButton[] buttons;
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{    
            jPanel2.removeAll();
            jPanel2.repaint();
            jPanel2.revalidate();
            
        
            f = new Fila();
            int valor = Integer.parseInt(jTextField3.getText());
            f.setBotoes(valor);
                        
            buttons = new JButton[valor];
            for(int i=0 ; i< valor; i++){
                buttons[i] = new JButton();
                buttons[i].setBounds(30+80*i,80,80,45);
                buttons[i].setBackground(Color.LIGHT_GRAY);
                buttons[i].setForeground(Color.red);
                buttons[i].setRolloverEnabled(false);
                buttons[i].setFocusTraversalPolicyProvider(false);
                buttons[i].setFont(new Font("Tahoma",Font.BOLD,15));
                jPanel2.add(buttons[i]); // adiciona botão ao painel
            }
              
            
            if(valor > 13){
               Dimension d = new Dimension(30+80*(valor+1),227);
               jPanel2.setPreferredSize(d);
               jPanel2.setSize(d);
            }else{
               Dimension d = new Dimension(1114,227);
               jPanel2.setPreferredSize(d);
               jPanel2.setSize(d); 
            }
            jTextField3.setText("");
            jPanel2.setVisible(true);
            jPanel2.revalidate();//método para atualizar o painel
        }catch(Exception e){
            //JOptionPane.showMessageDialog(null, "Ocorreu um erro.");
        }
        
        
    }//GEN-LAST:event_jButton4ActionPerformed


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{   
            if(buttons[0].getText() == ""){
                        buttons[0].setBackground(Color.LIGHT_GRAY);
                    }
            if(f.vazia()){
                    JOptionPane.showMessageDialog(null, "A fila já está vazia!! ");
            }else{
                f.remove();
                for(int i =0; i<f.getBotoes();i++){
                    buttons[i].setText("");
                    
                }
                for(int i = f.primeiro(),n = 0; i<=f.ultimo();i++,n++){
                    buttons[n].setText(Integer.toString(f.valor(i)));
                }
                
            }
        }catch (Exception e){ }  
    }//GEN-LAST:event_jButton3ActionPerformed

